CREATE TABLE garden(_id INTEGER PRIMARY KEY AUTOINCREMENT);

CREATE TABLE bed(_id INTEGER PRIMARY KEY AUTOINCREMENT, gardenID INTEGER NOT NULL);

CREATE TABLE image(_id INTEGER PRIMARY KEY AUTOINCREMENT, gardenID INTEGER, bedID INTEGER, image BLOB);

CREATE TABLE layout(_id INTEGER PRIMARY KEY AUTOINCREMENT, bedID INTEGER NOT NULL, image BLOB);

CREATE TABLE note(_id INTEGER PRIMARY KEY AUTOINCREMENT,gardenID INTEGER, bedID INTEGER, subject STRING NOT NULL, note STRING NOT NULL, alert INTEGER NOT NULL);

CREATE TABLE history(_id INTEGER PRIMARY KEY AUTOINCREMENT, bedID INTEGER NOT NULL, plantHistoryID INTEGER, workHistoryID INTEGER, harvestHistoryID INTEGER);

CREATE TABLE plant_history(_id INTEGER PRIMARY KEY AUTOINCREMENT, date DATE NOT NULL, note STRING NOT NULL);

CREATE TABLE work_history(_id INTEGER PRIMARY KEY AUTOINCREMENT, date DATE NOT NULL, note STRING NOT NULL);

CREATE TABLE harvest_history(_id INTEGER PRIMARY KEY AUTOINCREMENT, date DATE NOT NULL, note STRING NOT NULL);